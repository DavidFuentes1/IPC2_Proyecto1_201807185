    def __init__(self, Codigo, Nombre):
        self.Codigo = Codigo
        self.Nombre = Nombre


    def getCodigo(self)    :
        return self.Codigo
    
    def getNombre(self):
        return self.Nombre
    
    def setCodigo(self, x):
        self.Codigo = x

    def setNombre(self, y):
        self.Nombre = y
    
                

        